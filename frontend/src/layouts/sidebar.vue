<script lang="ts" setup>
import { type MenuOption } from 'naive-ui'
import { computed, h, ref, watch, watchEffect } from 'vue'
import { RouterLink, useRoute } from 'vue-router'

import { useSidebarStore } from '@/store/sidebar'

import { Icon } from '../components'
import { useMenus } from '../composables/menus'

import type { Menu } from '../composables'

const store = useSidebarStore()
const menus = useMenus()

const mapping = (items: Menu[]): MenuOption[] =>
  items.map(item => ({
    ...item,
    key: item.id,
    label: item.name != null ? () => h(RouterLink, { to: item }, { default: () => item.label }) : item.label,
    icon: item.icon != null ? () => h(Icon, { type: item.icon }) : undefined,
    children: item.children && mapping(item.children)
  }))

const options = computed(() => (menus.value ? mapping(menus.value) : []))

const route = useRoute()
const currentKey = ref<string>('')

// 从本地存储恢复展开状态
const getInitialExpandedKeys = (): string[] => {
  const saved = localStorage.getItem('sidebar-expanded-keys')
  return saved ? JSON.parse(saved) : []
}

const expandedKeys = ref<string[]>(getInitialExpandedKeys())

// 保存展开状态到本地存储
watch(
  expandedKeys,
  newKeys => {
    localStorage.setItem('sidebar-expanded-keys', JSON.stringify(newKeys))
  },
  { deep: true }
)

const routeMatched = (menu: Menu): boolean => {
  return route.name === menu.name && (menu.params == null || JSON.stringify(route.params) === JSON.stringify(menu.params))
}

const matchExpanded = (items: Menu[]): boolean => {
  let matched = false
  for (const item of items) {
    if (item.children != null) {
      if (matchExpanded(item.children)) {
        // 只在当前路由匹配且未展开时才添加
        if (!expandedKeys.value.includes(item.id)) {
          expandedKeys.value.push(item.id)
        }
      }
    }
    if (routeMatched(item)) {
      currentKey.value = item.id
      matched = true
    }
  }
  return matched
}

// 监听路由变化，更新当前选中和展开状态
watch(
  route,
  () => {
    currentKey.value = ''

    // 重新匹配当前路由并自动展开相关菜单
    if (menus.value) {
      matchExpanded(menus.value)
    }
  },
  { immediate: true }
)

watchEffect(() => {
  if (menus.value) {
    matchExpanded(menus.value)
  }
})
</script>

